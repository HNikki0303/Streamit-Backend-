const logoutUser = asyncHandler(async(req, res) => {
    await User.findByIdAndUpdate(
        //req mein ye user field isliye aa rha hai kyuki verifyJWT mein vo add ho rha hai auth middleware mein 
        req.user._id,
        {
            $unset: {
                refreshToken: 1 // this removes the field from document
            }
        },
        {
            new: true
        }
    )

    const options = {
        httpOnly: true,
        secure: true
    }

    return res
    .status(200)
    .clearCookie("accessToken", options)
    .clearCookie("refreshToken", options)
    .json(new ApiResponse(200, {}, "User logged Out"))
})

const refreshAccessToken = asyncHandler(async (req, res) => {
    const incomingRefreshToken = req.cookies.refreshToken || req.body.refreshToken

    //agar login hoga tabhi to refresh token hoga iske paas
    if (!incomingRefreshToken) {
        throw new ApiError(401, "unauthorized request")
    }//kehne ka matlab user login nahi hai tabhi to cookie mein refreshToken 

    try {
        //tokens ko to jwt se hi verify karenge uske secret token ki madad se
        const decodedToken = jwt.verify(
            incomingRefreshToken,
            process.env.REFRESH_TOKEN_SECRET
        )
        //jwt.verify karke hamein decodedToken se object aayega
        const user = await User.findById(decodedToken?._id)
        //kyuki ye object hoga to ismein _id bhi hogi 
        if (!user) {
            throw new ApiError(401, "Invalid refresh token")
        }
        //jo hamein refreshToken user se milega aur database se milega agar vo dono same nahi honge to error show karenge 
        if (incomingRefreshToken !== user?.refreshToken) {
            throw new ApiError(401, "Refresh token is expired or used")//#%?keval expire hona chahiye the na what is this used ??? 
        }
        //#%? but why will these two tokens not match
        const options = {
            httpOnly: true,
            secure: true
        }
    
        const {accessToken, newRefreshToken} = await generateAccessAndRefereshTokens(user._id)
    
        return res
        .status(200)
        .cookie("accessToken", accessToken, options)
        .cookie("refreshToken", newRefreshToken, options)
        .json(
            new ApiResponse(
                200, 
                {accessToken, refreshToken: newRefreshToken},
                "Access token refreshed"
            )
        )
    } catch (error) {
        throw new ApiError(401, error?.message || "Invalid refresh token")
    }

})

